export default function App() {
  const [project, setProject] = useState(null);
  const [panelOrder, setPanelOrder] = useState(readPanelOrder);
  const [settingsOpen, setSettingsOpen] = useState(false);
  const [expandedPanelId, setExpandedPanelId] = useState(null);
  const [highDensity, setHighDensity] = useState(readHighDensity);
  const [trendWindowMin, setTrendWindowMin] = useState(readTrendWindowMin);

  const isElectron = typeof window.dashboard !== 'undefined';
  const projectId = isElectron ? (project?.id ?? null) : 'default';
  const { metrics, connected } = useMetrics(projectId, trendWindowMin);

  const sensors = useSensors(
    useSensor(PointerSensor, { activationConstraint: { distance: 6 } }),
    useSensor(KeyboardSensor, { coordinateGetter: sortableKeyboardCoordinates }),
  );

  useEffect(() => {
    if (!isElectron) return undefined;
    window.dashboard.getActiveProject().then(setProject);
    const unsubscribe = window.dashboard.onProjectChanged(setProject);
    return unsubscribe;
  }, [isElectron]);

  const handleOpen = useCallback(async () => {
    if (!isElectron) return;
    const opened = await window.dashboard.openProject();
    if (opened) setProject(opened);
  }, [isElectron]);

  const handleSwitch = useCallback(async (id) => {
    if (!isElectron) return;
    const switched = await window.dashboard.switchProject(id);
    if (switched) setProject(switched);
  }, [isElectron]);

  const handleDragEnd = useCallback((event) => {
    const { active, over } = event;
    if (over && active.id !== over.id) {
      setPanelOrder((prev) => {
        const oldIndex = prev.indexOf(active.id);
        const newIndex = prev.indexOf(over.id);
        const next = arrayMove(prev, oldIndex, newIndex);
        savePanelOrder(next);
        return next;
      });
    }
  }, []);

  const handleResetLayout = useCallback(() => {
    setPanelOrder(DEFAULT_PANEL_ORDER);
    savePanelOrder(DEFAULT_PANEL_ORDER);
    setSettingsOpen(false);
  }, []);

  const handleHighDensityChange = useCallback((value) => {
    setHighDensity(value);
    saveHighDensity(value);
  }, []);

  const handleTrendWindowMinChange = useCallback((value) => {
    setTrendWindowMin(value);
    saveTrendWindowMin(value);
  }, []);

  const handleExpandPanel = useCallback((id) => {
    setExpandedPanelId(id);
  }, []);

  const handleCloseExpandedPanel = useCallback(() => {
    setExpandedPanelId(null);
  }, []);

  const showEmptyState = isElectron && !project;

  const panels = {
    events: {
      title: 'Events',
      subtitle: 'Live hook event feed',
      tooltip: METRIC_TOOLTIPS.events,
      render: () => <Events events={metrics.eventFeed} />,
    },
    'agent-state': {
      title: 'Agent State Distribution',
      subtitle: 'Metric #1 · Horizontal bar chart',
      tooltip: METRIC_TOOLTIPS['agent-state'],
      render: () => <AgentStateBarChart data={metrics.agentStateDistribution} />,
    },
    'security-block': {
      title: 'Security Block Rate',
      subtitle: 'Metric #2 · Policy gauge',
      tooltip: METRIC_TOOLTIPS['security-block'],
      render: () => <SecurityGauge {...metrics.securityBlockRate} />,
    },
    'human-loop': {
      title: 'Human-in-the-Loop',
      subtitle: 'Metric #10 · Manual approval friction',
      tooltip: METRIC_TOOLTIPS['human-loop'],
      render: () => <HumanInterventions data={metrics.humanInterventions} />,
    },
    'think-time': {
      title: 'Average Think Time',
      subtitle: 'Metric #3 · Reasoning latency',
      tooltip: METRIC_TOOLTIPS['think-time'],
      render: () => <ThinkTimeLine data={metrics.thinkTimeSeries} />,
    },
    'think-time-gauge': {
      title: 'Think Time Trend',
      subtitle: 'Metric #3 · Reasoning latency trend',
      tooltip: METRIC_TOOLTIPS['think-time-gauge'],
      render: () => <ThinkTimeGauge {...metrics.thinkTimeSummary} />,
    },
    'shell-outcome': {
      title: 'Shell Success vs Failure',
      subtitle: 'Metric #4 · Exit code time series',
      tooltip: METRIC_TOOLTIPS['shell-outcome'],
      render: () => <ShellOutcomeArea data={metrics.shellOutcomeSeries} />,
    },
    'shell-outcome-gauge': {
      title: 'Success vs Failure Trend',
      subtitle: 'Metric #4 · Shell exit code trend',
      tooltip: METRIC_TOOLTIPS['shell-outcome-gauge'],
      render: () => <ShellOutcomeGauge {...metrics.shellOutcomeSummary} />,
    },
    'code-churn': {
      title: 'Code Churn Volume',
      subtitle: 'Metric #8 · Lines added/removed',
      tooltip: METRIC_TOOLTIPS['code-churn'],
      render: () => <CodeChurnLine data={metrics.codeChurnSeries} />,
    },
    'code-churn-gauge': {
      title: 'Code Churn Trend',
      subtitle: 'Metric #8 · Edit velocity trend',
      tooltip: METRIC_TOOLTIPS['code-churn-gauge'],
      render: () => <CodeChurnGauge {...metrics.codeChurnSummary} />,
    },
    'session-scatter': {
      title: 'Autonomous Loop Duration',
      subtitle: 'Metric #9 · Session scatter plot',
      tooltip: METRIC_TOOLTIPS['session-scatter'],
      render: () => <SessionScatter data={metrics.sessionScatter} />,
    },
    'blast-radius': {
      title: 'Project Blast Radius',
      subtitle: 'Metric #5 · Directory heatmap',
      tooltip: METRIC_TOOLTIPS['blast-radius'],
      render: () => <BlastRadiusTreemap data={metrics.blastRadius} />,
    },
    'mcp-usage': {
      title: 'MCP Usage Breakdown',
      subtitle: 'Metric #6 · Tool call frequency',
      tooltip: METRIC_TOOLTIPS['mcp-usage'],
      render: () => <McpBarChart data={metrics.mcpUsage} />,
    },
  };

  const panelContent = Object.fromEntries(
    Object.entries(panels)
      .filter(([id]) => id !== 'events')
      .map(([id, panel]) => [
        id,
        (dragHandleProps) => (
          <Panel
            title={panel.title}
            subtitle={panel.subtitle}
            tooltip={panel.tooltip}
            dragHandleProps={dragHandleProps}
            onExpand={() => handleExpandPanel(id)}
          >
            {panel.render()}
          </Panel>
        ),
      ]),
  );

  const expandedPanel = expandedPanelId ? panels[expandedPanelId] : null;

  return (
    <DensityProvider dense={highDensity}>
      <div className="min-h-screen bg-[#0b0c10]">
      <ProjectBar
        project={project}
        onOpen={handleOpen}
        onSwitch={handleSwitch}
        connected={connected}
        highDensity={highDensity}
        onHighDensityChange={handleHighDensityChange}
        onSettingsOpen={() => setSettingsOpen(true)}
      />

      {!isElectron && (
        <header className="sticky top-0 z-10 border-b border-border bg-panel/90 backdrop-blur">
          <div className="relative mx-auto flex max-w-[1600px] items-center justify-between px-4 py-2">
            <h1 className="bg-gradient-to-r from-amber-300 via-orange-400 to-rose-400 bg-clip-text text-xl font-bold tracking-tight text-transparent">
              catadio
            </h1>
            <div className="flex items-center gap-4 text-sm">
              <DensityToggle checked={highDensity} onChange={handleHighDensityChange} />
              <div className="hidden sm:block text-right">
                <p className="text-slate-400">
                  {metrics.totals.recentEvents} events / {metrics.totals.sessions} sessions (1h)
                </p>
                <p className="text-xs text-slate-600">{metrics.totals.events} total buffered</p>
              </div>
              <div className="flex items-center gap-2">
                <span
                  className={`h-2 w-2 rounded-full ${connected ? 'bg-success animate-pulse' : 'bg-danger'}`}
                />
                <span className="text-slate-400">{connected ? 'Live' : 'Reconnecting…'}</span>
              </div>
              <button
                type="button"
                onClick={() => setSettingsOpen(true)}
                aria-label="Open settings"
                className="flex h-8 w-8 shrink-0 cursor-pointer items-center justify-center rounded-lg border border-white/10 bg-white/5 text-slate-400 transition hover:border-white/20 hover:bg-white/10 hover:text-white focus-visible:outline-none focus-visible:ring-2 focus-visible:ring-accent/50"
              >
                <svg viewBox="0 0 20 20" fill="currentColor" className="h-4 w-4" aria-hidden="true">
                  <path fillRule="evenodd" d="M2 4.75A.75.75 0 012.75 4h14.5a.75.75 0 010 1.5H2.75A.75.75 0 012 4.75zm0 5A.75.75 0 012.75 9h14.5a.75.75 0 010 1.5H2.75A.75.75 0 012 9.75zm0 5a.75.75 0 01.75-.75h14.5a.75.75 0 010 1.5H2.75a.75.75 0 01-.75-.75z" clipRule="evenodd" />
                </svg>
              </button>
            </div>
          </div>
        </header>
      )}

      <main className={`mx-auto max-w-[1600px] px-4 ${highDensity ? 'py-2' : 'py-4'}`}>
        {showEmptyState ? (
          <div className="flex min-h-[60vh] flex-col items-center justify-center text-center">
            <h2 className="text-2xl font-semibold text-white">No project selected</h2>
            <p className="mt-2 max-w-md text-slate-400">
              Open a Cursor project folder to install dashboard hooks and start streaming agent
              telemetry in real time.
            </p>
            <button
              type="button"
              onClick={handleOpen}
              className="mt-6 rounded-md bg-accent px-4 py-2 text-sm font-medium text-white hover:bg-accent/90"
            >
              Open project folder
            </button>
          </div>
        ) : (
          <>
            <Panel
              title={panels.events.title}
              subtitle={panels.events.subtitle}
              tooltip={panels.events.tooltip}
              className={highDensity ? 'mb-1.5' : 'mb-3'}
              onExpand={() => handleExpandPanel('events')}
            >
              {panels.events.render()}
            </Panel>

            <DndContext sensors={sensors} collisionDetection={closestCenter} onDragEnd={handleDragEnd}>
              <SortableContext items={panelOrder} strategy={rectSortingStrategy}>
                <div
                  className={`grid grid-cols-1 items-stretch sm:grid-cols-2 ${
                    highDensity
                      ? 'gap-1.5 lg:grid-cols-4 xl:grid-cols-6'
                      : 'gap-3 lg:grid-cols-3'
                  }`}
                >
                  {panelOrder.map((id) => (
                    <SortablePanel key={id} id={id}>
                      {({ dragHandleProps }) => panelContent[id]?.(dragHandleProps)}
                    </SortablePanel>
                  ))}
                </div>
              </SortableContext>
            </DndContext>
          </>
        )}
      </main>

      {expandedPanel && (
        <ExpandedPanelModal
          title={expandedPanel.title}
          subtitle={expandedPanel.subtitle}
          tooltip={expandedPanel.tooltip}
          onClose={handleCloseExpandedPanel}
        >
          <ExpandedProvider expanded>
            <DensityProvider dense={false}>{expandedPanel.render()}</DensityProvider>
          </ExpandedProvider>
        </ExpandedPanelModal>
      )}

      {settingsOpen && (
        <SettingsModal
          onClose={() => setSettingsOpen(false)}
          onResetLayout={handleResetLayout}
          highDensity={highDensity}
          onHighDensityChange={handleHighDensityChange}
          trendWindowMin={trendWindowMin}
          onTrendWindowMinChange={handleTrendWindowMinChange}
        />
      )}
      </div>
    </DensityProvider>
  );
}
