function readHighDensity() {
  try {
    return localStorage.getItem(HIGH_DENSITY_KEY) === 'true';
  } catch {
    return false;
  }
}

function saveHighDensity(value) {
  try {
    localStorage.setItem(HIGH_DENSITY_KEY, String(value));
  } catch {
    // Ignore storage errors
  }
}

function readTrendWindowMin() {
  try {
    const stored = localStorage.getItem(TREND_WINDOW_KEY);
    if (stored != null) {
      const parsed = Number(stored);
      if (Number.isFinite(parsed) && parsed > 0) return Math.min(parsed, 60);
    }
  } catch {
    // Ignore storage errors
  }
  return DEFAULT_TREND_WINDOW_MIN;
}

function saveTrendWindowMin(value) {
  try {
    localStorage.setItem(TREND_WINDOW_KEY, String(value));
  } catch {
    // Ignore storage errors
  }
}


function migratePanelOrder(stored) {
  if (!Array.isArray(stored)) return DEFAULT_PANEL_ORDER;

  const seen = new Set();
  const merged = [];
  for (const id of stored) {
    if (DEFAULT_PANEL_ORDER.includes(id) && !seen.has(id)) {
      merged.push(id);
      seen.add(id);
    }
  }
  for (const id of DEFAULT_PANEL_ORDER) {
    if (!seen.has(id)) merged.push(id);
  }
  return merged.length ? merged : DEFAULT_PANEL_ORDER;
}

function readPanelOrder() {
  try {
    const stored = localStorage.getItem(PANEL_ORDER_KEY);
    if (stored) {
      return migratePanelOrder(JSON.parse(stored));
    }
  } catch {
    // Ignore storage errors
  }
  return DEFAULT_PANEL_ORDER;
}

function savePanelOrder(order) {
  try {
    localStorage.setItem(PANEL_ORDER_KEY, JSON.stringify(order));
  } catch {
    // Ignore storage errors
  }
}

function SortablePanel({ id, children }) {
  const { attributes, listeners, setNodeRef, transform, transition, isDragging } = useSortable({ id });
  const style = {
    transform: CSS.Transform.toString(transform),
    transition,
    zIndex: isDragging ? 10 : undefined,
    opacity: isDragging ? 0.6 : 1,
  };
  return (
    <div ref={setNodeRef} style={style} className="h-full">
      {children({ dragHandleProps: { ...attributes, ...listeners } })}
    </div>
  );
}

function ExpandedPanelModal({ title, subtitle, tooltip, onClose, children }) {
  useEffect(() => {
    const handleKey = (e) => {
      if (e.key === 'Escape') onClose();
    };
    window.addEventListener('keydown', handleKey);
    return () => window.removeEventListener('keydown', handleKey);
  }, [onClose]);

  return (
    <div
      className="fixed inset-0 z-50 flex items-center justify-center bg-black/70 p-4 backdrop-blur-sm"
      onClick={onClose}
    >
      <div
        role="dialog"
        aria-modal="true"
        aria-labelledby="expanded-panel-title"
        className="flex h-[min(92vh,calc(100vh-2rem))] w-[60vw] flex-col overflow-hidden rounded-xl border border-border bg-surface shadow-2xl"
        onClick={(e) => e.stopPropagation()}
      >
        <div className="flex shrink-0 items-start justify-between gap-4 border-b border-border px-6 py-4">
          <div className="min-w-0">
            <h2 id="expanded-panel-title" className="text-lg font-semibold text-white">
              {title}
            </h2>
            {subtitle && <p className="mt-0.5 text-sm text-slate-500">{subtitle}</p>}
            {tooltip && <p className="mt-2 text-xs leading-relaxed text-slate-400">{tooltip}</p>}
          </div>
          <button
            type="button"
            onClick={onClose}
            aria-label="Close expanded panel"
            className="shrink-0 rounded-lg p-1 text-slate-500 hover:bg-white/5 hover:text-slate-300"
          >
            <svg viewBox="0 0 20 20" fill="currentColor" className="h-5 w-5" aria-hidden="true">
              <path d="M6.28 5.22a.75.75 0 00-1.06 1.06L8.94 10l-3.72 3.72a.75.75 0 101.06 1.06L10 11.06l3.72 3.72a.75.75 0 101.06-1.06L11.06 10l3.72-3.72a.75.75 0 00-1.06-1.06L10 8.94 6.28 5.22z" />
            </svg>
          </button>
        </div>
        <div className="flex min-h-0 flex-1 flex-col overflow-auto p-6">{children}</div>
      </div>
    </div>
  );
}

